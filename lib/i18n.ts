export type Locale = 'en' | 'zh';

export function resolveLocale(value: string | null | undefined): Locale {
  return value === 'zh' ? 'zh' : 'en';
}

export function localeToHtmlLang(locale: Locale) {
  return locale === 'zh' ? 'zh-CN' : 'en';
}

